              </tr>
              <tr >
                <td>4</td>
                <td>Mitosis</td>
                <td style="width: 800px;">Es la división celular característica de las células somáticas, que produce dos células hijas genéticamente idénticas a la célula progenitora.</td>
              </tr>
              <tr >
                <td>5</td>
                <td>Meiosis</td>
                <td style="width: 800px;">Es la división celular que tiene lugar durante la formación de los gametos en especies de reproducción sexual, mediante la cual una célula germinal diploide da lugar a cuatro células hijas haploides.</td>
              </tr>
              <tr >
                <td>6</td>
                <td>Alelo</td>
                <td style="width: 800px;">Cualquiera de las formas alternativas de un gen, es decir, uno de un par o serie de genes alternativos que aparecen en un locus determinado en un cromosoma.</td>
              </tr>
              <tr >
                <td>7</td>
                <td>Locus</td>
                <td style="width: 800px;">Es la posición o localización fija en un cromosoma ocupada por un determinado gen o por un par de sus alelos, cuyo plural es loci.
                </td>
              </tr>
              <tr >
                <td>8</td>
                <td>Homocigoto</td>
                <td style="width: 800px;">Célula o individuo diploide con alelos idénticos en uno o más loci de cromosomas homólogos.
                </td>
              </tr>
              <tr >
                <td>9</td>
                <td>Heterocigoto</td>
                <td style="width: 800px;">Célula o individuo diploide con alelos diferentes en uno o más loci de cromosomas homólogos.
                </td>
              </tr>
              <tr >
                <td>10</td>
                <td>Dominante</td>
                <td style="width: 800px;">Alelo que muestra su efecto sobre el fenotipo no solo cuando se presentan en combinación homocigótica, sino también en combinación heterocigótica.
                </td>
              </tr>
              <tr >
                <td>11</td>
                <td>Recesivo</td>
                <td style="width: 800px;">Alelo que carece de efecto sobre el fenotipo a menos que este se encuentre en combinación homocigótica en el genotipo, es decir, que se presenta en forma de doble recesivo.
                </td>
              </tr>
              <tr >
                <td>12</td>
                <td>Fenotipo</td>
                <td style="width: 800px;">Características físicas de un organismo están afectadas simultáneamente por el genotipo y las condiciones del medio. Por ejemplo, el olor del pelo de los cobayos, la presencia de lóbulos libres en las orejas, el cabello lacio o rizado, etc.
                </td>
              </tr>
              <tr >
                <td>13</td>
                <td>Genotipo</td>
                <td style="width: 800px;">Composición genética de un organismo. Es función del número y tipo de genes presentes en cada célula.
                </td>
              </tr>
              <tr >
                <td>14</td>
                <td>Cruzamiento</td>
                <td style="width: 800px;">Tipo de apareamiento en el que participan individuos pertenecientes a una misma especie pero que no están relacionados de modo muy estrecho y son genéticamente distintos.
                </td>
              </tr>
